, AGE_RESTRICTION INTEGER
, ALCOHOL_PERCENT NUMBER
, PRODUCT_CATEGORY INTEGER
, CONSTRAINT PRODUCT_PK PRIMARY KEY
  (
    ID
  )
  ENABLE
);

